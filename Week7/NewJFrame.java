    private void chatBotActionPerformed(java.awt.event.ActionEvent evt) {                                        
        // TODO add your handling code here:
        try{
        //create URI object with URL from copilot
        URI url = new URI("https://sandersonsai.ai.copilot.live");
        if (Desktop.isDesktopSupported()){
            //check if Desktop is supported
            Desktop desktop = Desktop.getDesktop();
            //check if browser action is supported
            if (desktop.isSupported(Desktop.Action.BROWSE)){
                desktop.browse(url);
            }else {
                System.out.println("BROWSE action not supported.");
            }
        }else{
            System.out.println("Desktop not supported.");
        } 
        } catch (Exception e){
            e.printStackTrace();
        }
    }                                       
